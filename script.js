function applyPreviewFromUrl() {
  const params = new URLSearchParams(window.location.search);
  const path = params.get("path");
  const screen = params.get("screen");
  const questionParam = Number.parseInt(params.get("q") || "1", 10);

  if (screen === "quiz") {
    const questionIndex = Number.isNaN(questionParam)
      ? 0
      : Math.min(Math.max(questionParam - 1, 0), questions.length - 1);
    state.currentQuestion = questionIndex;
    renderQuestion();
    showScreen("quiz");
    return;
  }

  if (!pathways[path]) {
    return;
  }

  prefillAnswersForPath(path);
  renderResult();

  if (screen === "email") {
    renderEmailPreview();
    showScreen("email");
    return;
  }

  if (screen === "result") {
    showScreen("result");
  }
}

