function showScreen(screenKey) {
  const applyScreenChange = () => {
    Object.values(screens).forEach((screen) => screen.classList.remove("screen-active"));
    screens[screenKey].classList.add("screen-active");
  };

  if (document.startViewTransition) {
    document.startViewTransition(applyScreenChange);
  } else {
    applyScreenChange();
  }

  window.scrollTo({ top: 0, behavior: "smooth" });
}

function setMediaImage(element, imagePath) {
  if (!element) {
    return;
  }

  if (!imagePath) {
    element.style.backgroundImage = "";
    element.classList.remove("media-slot-has-image", "mini-visual-has-image");
    return;
  }

  element.style.backgroundImage = `url('${imagePath}')`;
  if (element.classList.contains("media-slot")) {
    element.classList.add("media-slot-has-image");
  }
  if (element.classList.contains("mini-visual")) {
    element.classList.add("mini-visual-has-image");
  }
}

function renderQuestion() {
  const question = questions[state.currentQuestion];
  const selected = state.answers[state.currentQuestion];
  questionKicker.textContent = `Question ${state.currentQuestion + 1}`;
  questionPrompt.textContent = question.prompt;
  questionNote.textContent = question.note;
  questionVisualLabel.textContent = question.visualLabel || "Image Placeholder";
  questionVisualTitle.textContent = question.visualTitle || "Question editorial image";
  questionVisualCopy.textContent =
    question.visualCopy ||
    "Replace this area with an image, illustration, or quiet still-life that supports the emotional tone of the question.";
  setMediaImage(questionMediaSlot, question.image);
  progressLabel.textContent = `Question ${state.currentQuestion + 1} of ${questions.length}`;
  progressFill.style.width = `${((state.currentQuestion + 1) / questions.length) * 100}%`;
  nextButton.disabled = selected === null;
  nextButton.textContent =
    state.currentQuestion === questions.length - 1 ? "See My Pathway" : "Next";
  backButton.disabled = state.currentQuestion === 0;
  answerList.innerHTML = "";

  question.options.forEach((option, optionIndex) => {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "answer-option";
    if (selected === optionIndex) {
      button.classList.add("selected");
    }
    button.style.setProperty("--item-index", optionIndex);
    button.innerHTML = `
      <span class="answer-badge">${String.fromCharCode(65 + optionIndex)}</span>
      <span class="answer-text">${option.label}</span>
    `;
    button.addEventListener("click", () => {
      state.answers[state.currentQuestion] = optionIndex;
      renderQuestion();
    });
    answerList.appendChild(button);
  });
}

function computeScores() {
  const scores = {
    mindfulness: 0,
    compassion: 0,
    interconnection: 0,
    creativity: 0,
    foundations: 0
  };

  state.answers.forEach((answerIndex, questionIndex) => {
    if (answerIndex === null) {
      return;
    }
    const option = questions[questionIndex].options[answerIndex];
    const bonus = questions[questionIndex].priority ? 0.35 : 0;
    scores[option.path] += 1 + bonus;
  });

  return scores;
}

function determinePathway(scores) {
  const sorted = Object.entries(scores).sort((left, right) => right[1] - left[1]);
  const topScore = sorted[0][1];
  const tied = sorted.filter((entry) => entry[1] === topScore).map((entry) => entry[0]);

  if (tied.length === 1) {
    return tied[0];
  }

  const priorityOrder = [1, 5, 3, 0, 4, 2];
  for (const questionIndex of priorityOrder) {
    const answerIndex = state.answers[questionIndex];
    if (answerIndex === null) {
      continue;
    }
    const candidate = questions[questionIndex].options[answerIndex].path;
    if (tied.includes(candidate)) {
      return candidate;
    }
  }

  return sorted[0][0];
}

function renderResult() {
  const scores = computeScores();
  const resultKey = determinePathway(scores);
  const pathway = pathways[resultKey];
  state.resultKey = resultKey;

  resultTitle.textContent = pathway.title;
  resultSummary.textContent = pathway.summary;
  setMediaImage(resultMediaSlot, pathway.resultImage);
  resultVisualTitle.textContent = pathway.title;
  resultVisualCopy.textContent = "Add a quiet editorial image here.";
  deckTitle.textContent = pathway.deckTitle;
  deckCopy.textContent = pathway.deckCopy;
  deckVisual.textContent = "";
  setMediaImage(deckVisual, pathway.deckImage);
  eventTitle.textContent = pathway.eventTitle;
  eventCopy.textContent = pathway.eventCopy;
  eventVisual.textContent = "";
  setMediaImage(eventVisual, pathway.eventImage);
}

